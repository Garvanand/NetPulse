from fastapi import APIRouter, Depends, Query
from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy import select, func

from app.api.incidents.schemas import PaginatedIncidents
from app.core.dependencies import get_db_session
from app.core.security import get_current_active_user
from app.core.rate_limit import RateLimiter
from app.db.models import IncidentModel, UserModel

router = APIRouter(prefix="/predictions", tags=["predictions"])

@router.get("/", response_model=PaginatedIncidents)
async def list_predictions(
    min_score: float = Query(0.5, ge=0.0, le=1.0, description="Minimum prediction score threshold"),
    page: int = Query(1, ge=1),
    size: int = Query(20, ge=1, le=100),
    session: AsyncSession = Depends(get_db_session),
    user: UserModel = Depends(get_current_active_user),
    rate_limit: None = Depends(RateLimiter(requests=60, window=60))
):
    """List predicted upcoming incidents generated by the ML engine."""
    # Predictions are just incidents that haven't fully materialized, often with high prediction_score
    base_stmt = select(IncidentModel).where(IncidentModel.prediction_score >= min_score)
        
    count_stmt = select(func.count()).select_from(base_stmt.subquery())
    total = await session.scalar(count_stmt) or 0
    
    stmt = base_stmt.order_by(IncidentModel.detected_at.desc()).offset((page - 1) * size).limit(size)
    result = await session.execute(stmt)
    items = result.scalars().all()
    
    return PaginatedIncidents(items=items, total=total, page=page, size=size)
